#include <unistd.h>

int	ft_str_is_numeric(char *str)
{
	int	i;
	int is;

	i = 0;
	is = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= '0' && str[i] <= '9'))
			is = 1;
		else
			return (0);
		i++;
	}
	return (is);
}
