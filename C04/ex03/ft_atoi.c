int	ft_atoi(char *str)
{
	int	i;
	int	sym;
	int	aws;

	i = 0;
	sym = 1;
	aws = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
		{
			sym *= -1;
		}
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		aws = (aws * 10) + (str[i] - '0');
		i++;
	}
	return (aws * sym);
}
